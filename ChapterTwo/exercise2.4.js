let numA = 328;
let numB = 69;
let numC = 420;

console.log(`${numA} + ${numB} is ${numA += numB}`);                    // adds numB to numA
console.log(`${numA} / ${numC} is ${numA /= numC}`);                    // Divides numA by numC
console.log(`The modulus of ${numC} and ${numB} is ${numC %= numB}`)    // Takes the modulus of numC and numB